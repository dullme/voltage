        var t = document.documentElement.scrollTop||document.body.scrollTop;
        //当滚动距离变大时,头部消失
        //当滚动距离变小时,头部出现
        if(x > t){
            var index = getIndex()
            if(index >0){

            }else{
                $('header').removeClass('headerOut')
                $('header').addClass('headerIn')
            }
        }else{
            if(t < 100){
                return
            }
            var index = getIndex()
            if(index > 0){

            }else{
                $('header').removeClass('headerIn')
                $('header').addClass('headerOut')
            }
        }
        x = t
    }
    function getIndex(){
        var index = 0
        $('.getActive').each(function(){
            $(this.children).each(function(){
                if(this.children[2].className.indexOf('liActive')>-1){
                    index ++
                }
            })
        })
        return index
    }
    $('.clearfix').on('mouseenter', 'li', function() {//绑定鼠标进入事件
        $(this.children[2]).addClass('liActive')
    });
    $('.clearfix').on('mouseleave', 'li', function() {//绑定鼠标划出事件
        $(this.children[2]).removeClass('liActive')
    });
